package fr.iutvalence.projetS2.java.groupe7;

/**
 * Position, type pour relier deux valeurs a une position 
 * sur la carte suivant l'axe x et y
 * @author chevrotl
 *
 */
public class Position
{
	public float X ;
	public float Y ;
	
	public Position()
	{
		this.X = 0 ;
		this.Y = 0;
	}
	
	public Position(float x, float y)
	{
		this.X = x ;
		this.Y = y ;
	}

	
}
